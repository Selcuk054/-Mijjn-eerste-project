package P07_Forloop;

public class Q02_Forloop {
    public static void main(String[] args) {
        //10 ile 30 arasindaki sayilari (10 ve 30 dahil) aralarinda virgul kullanarak ayni satirda yazdirin.
        for (int i = 10; i <=30 ; i++) {
            System.out.print(i+",");

        }
    }
}
