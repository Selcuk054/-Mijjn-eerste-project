package P07_Forloop;

public class Q03_Forloop {
    public static void main(String[] args) {
        // 100 den baslayarak 50 ye kadar 50 dahil olacak sekilde sayilari aralarinda virgul
        //olacak sekilde ayni satirda yazdirin

        for (int i = 100; i >=50 ; i--) {
            System.out.print(i+",");

        }
    }

}
