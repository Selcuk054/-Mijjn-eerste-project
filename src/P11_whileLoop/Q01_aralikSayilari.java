package P11_whileLoop;

public class Q01_aralikSayilari {
    public static void main(String[] args) {
        // While loop kullanarak 3 den 13 e kadar tum tek tam sayilari ekrana yazdiriniz.

        int sayi=0;

        while (sayi<=13){
            if (sayi>=3){
                System.out.print(sayi+" ");
            }
            sayi++;


        }



    }
}
